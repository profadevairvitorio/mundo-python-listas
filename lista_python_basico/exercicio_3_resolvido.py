# Peça um número inteiro e mostre o dobro dele.
# Solicitar ao usuário um número inteiro
# A função input() recebe o número como uma string, então usamos int() para converter a string em um número inteiro.
numero = int(input("Digite um número inteiro: "))

# Calcular o dobro do número
# Multiplicamos o número por 2 para obter o dobro.
dobro = numero * 2

# Exibir o resultado do dobro
# A função print() exibe a mensagem junto com o valor do dobro calculado.
print("O dobro do número é:", dobro)
